package Data;

public class Calculator {

    public float sum(float x, float y){
        return x + y;
    }

    public float substract(float x, float y){
        return  x - y;
    }


    public float multiplication(float x, float y){
        return x * y;
    }

    public float division(float x, float y) {
        return x / y;
    }
    float pw2(float x){
        return x*x;
    }
}
